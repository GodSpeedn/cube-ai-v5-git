    """Main configuration function"""
    print("ğŸš€ GitHub Configuration Setup")
    print("=" * 50)
    
    # Check if already configured
    if os.getenv('GITHUB_TOKEN') and os.getenv('GITHUB_USERNAME'):
        print("âœ… GitHub already configured!")
